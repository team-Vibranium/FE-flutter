import 'package:flutter/material.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import '../core/providers/puzzle_provider.dart';
import '../core/models/puzzle_mission.dart';

class MissionScreen extends ConsumerStatefulWidget {
  final String alarmTitle;
  final VoidCallback onMissionCompleted;
  final VoidCallback? onMissionFailed;

  const MissionScreen({
    super.key,
    required this.alarmTitle,
    required this.onMissionCompleted,
    this.onMissionFailed,
  });

  @override
  ConsumerState<MissionScreen> createState() => _MissionScreenState();
}

class _MissionScreenState extends ConsumerState<MissionScreen>
    with TickerProviderStateMixin {
  late AnimationController _pulseController;
  late AnimationController _fadeController;
  late Animation<double> _pulseAnimation;
  late Animation<double> _fadeAnimation;

  @override
  void initState() {
    super.initState();
    _setupAnimations();
    
    // ÎØ∏ÏÖò ÏãúÏûë
    WidgetsBinding.instance.addPostFrameCallback((_) {
      ref.read(puzzleStateProvider.notifier).startNewPuzzle();
    });
  }

  void _setupAnimations() {
    _pulseController = AnimationController(
      duration: const Duration(seconds: 2),
      vsync: this,
    );
    _fadeController = AnimationController(
      duration: const Duration(milliseconds: 800),
      vsync: this,
    );

    _pulseAnimation = Tween<double>(
      begin: 1.0,
      end: 1.1,
    ).animate(CurvedAnimation(
      parent: _pulseController,
      curve: Curves.easeInOut,
    ));

    _fadeAnimation = Tween<double>(
      begin: 0.0,
      end: 1.0,
    ).animate(CurvedAnimation(
      parent: _fadeController,
      curve: Curves.easeIn,
    ));

    _pulseController.repeat(reverse: true);
    _fadeController.forward();
  }

  @override
  void dispose() {
    _pulseController.dispose();
    _fadeController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final puzzleState = ref.watch(puzzleStateProvider);
    final puzzleNotifier = ref.read(puzzleStateProvider.notifier);

    // ÎØ∏ÏÖò ÏôÑÎ£å Ïãú Ï≤òÎ¶¨
    if (puzzleState.isCompleted && puzzleState.isCorrect) {
      WidgetsBinding.instance.addPostFrameCallback((_) {
        _showSuccessDialog();
      });
    }

    return Scaffold(
      backgroundColor: Colors.indigo.shade50,
      appBar: AppBar(
        title: Text('üß© ÎØ∏ÏÖò Ìï¥Í≤∞'),
        backgroundColor: Colors.indigo,
        foregroundColor: Colors.white,
        elevation: 0,
        automaticallyImplyLeading: false, // Îí§Î°úÍ∞ÄÍ∏∞ Î≤ÑÌäº Ï†úÍ±∞
      ),
      body: puzzleState.currentMission == null
          ? const Center(child: CircularProgressIndicator())
          : _buildMissionContent(puzzleState, puzzleNotifier),
    );
  }

  Widget _buildMissionContent(PuzzleState puzzleState, PuzzleNotifier puzzleNotifier) {
    final mission = puzzleState.currentMission!;

    return SafeArea(
      child: Padding(
        padding: const EdgeInsets.all(20.0),
        child: Column(
          children: [
            // Ìó§Îçî
            _buildHeader(),
            const SizedBox(height: 30),

            // ÎØ∏ÏÖò ÌÉÄÏûÖ ÌëúÏãú
            _buildMissionTypeCard(mission.type),
            const SizedBox(height: 30),

            // ÏßàÎ¨∏
            _buildQuestionCard(mission.question),
            const SizedBox(height: 40),

            // ÏÑ†ÌÉùÏßÄÎì§ (Ïä§ÌÅ¨Î°§ Í∞ÄÎä•ÌïòÍ≤å Î≥ÄÍ≤Ω)
            Expanded(
              child: SingleChildScrollView(
                child: _buildOptionsGrid(mission.options, puzzleState, puzzleNotifier),
              ),
            ),

            // ÌïòÎã® Î≤ÑÌäºÎì§
            _buildBottomButtons(puzzleState, puzzleNotifier),
          ],
        ),
      ),
    );
  }

  Widget _buildHeader() {
    return FadeTransition(
      opacity: _fadeAnimation,
      child: Column(
        children: [
          AnimatedBuilder(
            animation: _pulseAnimation,
            builder: (context, child) {
              return Transform.scale(
                scale: _pulseAnimation.value,
                child: Container(
                  width: 80,
                  height: 80,
                  decoration: BoxDecoration(
                    shape: BoxShape.circle,
                    color: Colors.indigo,
                    boxShadow: [
                      BoxShadow(
                        color: Colors.indigo.withOpacity(0.3),
                        blurRadius: 15,
                        spreadRadius: 5,
                      ),
                    ],
                  ),
                  child: const Icon(
                    Icons.psychology,
                    color: Colors.white,
                    size: 40,
                  ),
                ),
              );
            },
          ),
          const SizedBox(height: 16),
          Text(
            '${widget.alarmTitle} ÏïåÎûå',
            style: const TextStyle(
              fontSize: 20,
              fontWeight: FontWeight.bold,
              color: Colors.indigo,
            ),
          ),
          const SizedBox(height: 8),
          const Text(
            'ÎØ∏ÏÖòÏùÑ Ìï¥Í≤∞ÌïòÎ©¥ ÏïåÎûåÏù¥ Ìï¥Ï†úÎê©ÎãàÎã§',
            style: TextStyle(
              fontSize: 16,
              color: Colors.grey,
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildMissionTypeCard(PuzzleType type) {
    String typeText;
    IconData typeIcon;
    Color typeColor;

    switch (type) {
      case PuzzleType.numberSequence:
        typeText = 'Ïà´Ïûê ÏàúÏÑú';
        typeIcon = Icons.format_list_numbered;
        typeColor = Colors.blue;
        break;
      case PuzzleType.patternMatch:
        typeText = 'Ìå®ÌÑ¥ Îß§Ïπ≠';
        typeIcon = Icons.pattern;
        typeColor = Colors.green;
        break;
      case PuzzleType.colorSequence:
        typeText = 'ÏÉâÏÉÅ ÏàúÏÑú';
        typeIcon = Icons.palette;
        typeColor = Colors.orange;
        break;
    }

    return FadeTransition(
      opacity: _fadeAnimation,
      child: Container(
        padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 12),
        decoration: BoxDecoration(
          color: typeColor.withOpacity(0.1),
          borderRadius: BorderRadius.circular(25),
          border: Border.all(color: typeColor.withOpacity(0.3)),
        ),
        child: Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            Icon(typeIcon, color: typeColor, size: 20),
            const SizedBox(width: 8),
            Text(
              typeText,
              style: TextStyle(
                color: typeColor,
                fontWeight: FontWeight.w600,
                fontSize: 16,
              ),
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildQuestionCard(String question) {
    return FadeTransition(
      opacity: _fadeAnimation,
      child: Container(
        width: double.infinity,
        padding: const EdgeInsets.all(24),
        decoration: BoxDecoration(
          color: Colors.white,
          borderRadius: BorderRadius.circular(16),
          boxShadow: [
            BoxShadow(
              color: Colors.black.withOpacity(0.1),
              blurRadius: 10,
              offset: const Offset(0, 4),
            ),
          ],
        ),
        child: Text(
          question,
          style: const TextStyle(
            fontSize: 18,
            fontWeight: FontWeight.w600,
            color: Colors.black87,
            height: 1.4,
          ),
          textAlign: TextAlign.center,
        ),
      ),
    );
  }

  Widget _buildOptionsGrid(
    List<String> options, 
    PuzzleState puzzleState, 
    PuzzleNotifier puzzleNotifier
  ) {
    return GridView.builder(
      shrinkWrap: true,
      physics: const NeverScrollableScrollPhysics(),
      gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
        crossAxisCount: 2,
        crossAxisSpacing: 16,
        mainAxisSpacing: 16,
        childAspectRatio: 1.5, // ÎÜíÏù¥Î•º Ï§ÑÏó¨ÏÑú Í≥µÍ∞Ñ Ï†àÏïΩ
      ),
      itemCount: options.length,
      itemBuilder: (context, index) {
        final option = options[index];
        final isSelected = puzzleState.selectedAnswer == option;
        final isCompleted = puzzleState.isCompleted;

        Color cardColor;
        Color textColor;
        IconData? iconData;

        if (isCompleted) {
          if (option == puzzleState.currentMission!.correctAnswer) {
            // Ï†ïÎãµ
            cardColor = Colors.green;
            textColor = Colors.white;
            iconData = Icons.check_circle;
          } else if (isSelected) {
            // ÏÑ†ÌÉùÌñàÏßÄÎßå ÌãÄÎ¶∞ Îãµ
            cardColor = Colors.red;
            textColor = Colors.white;
            iconData = Icons.cancel;
          } else {
            // ÏÑ†ÌÉùÌïòÏßÄ ÏïäÏùÄ Ïò§Îãµ
            cardColor = Colors.grey.shade300;
            textColor = Colors.grey.shade600;
          }
        } else {
          // ÏïÑÏßÅ ÏôÑÎ£åÎêòÏßÄ ÏïäÏùå
          if (isSelected) {
            cardColor = Colors.indigo;
            textColor = Colors.white;
          } else {
            cardColor = Colors.white;
            textColor = Colors.black87;
          }
        }

        return FadeTransition(
          opacity: _fadeAnimation,
          child: GestureDetector(
            onTap: isCompleted 
                ? null 
                : () => puzzleNotifier.selectAnswer(option),
            child: AnimatedContainer(
              duration: const Duration(milliseconds: 200),
              decoration: BoxDecoration(
                color: cardColor,
                borderRadius: BorderRadius.circular(16),
                border: Border.all(
                  color: isSelected && !isCompleted 
                      ? Colors.indigo 
                      : Colors.grey.shade300,
                  width: 2,
                ),
                boxShadow: [
                  if (!isCompleted || option == puzzleState.currentMission!.correctAnswer)
                    BoxShadow(
                      color: cardColor.withOpacity(0.3),
                      blurRadius: 8,
                      offset: const Offset(0, 4),
                    ),
                ],
              ),
              child: Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  if (iconData != null) ...[
                    Icon(
                      iconData,
                      color: textColor,
                      size: 32,
                    ),
                    const SizedBox(height: 8),
                  ],
                  Text(
                    option,
                    style: TextStyle(
                      fontSize: 24,
                      fontWeight: FontWeight.bold,
                      color: textColor,
                    ),
                    textAlign: TextAlign.center,
                  ),
                ],
              ),
            ),
          ),
        );
      },
    );
  }

  Widget _buildBottomButtons(PuzzleState puzzleState, PuzzleNotifier puzzleNotifier) {
    return FadeTransition(
      opacity: _fadeAnimation,
      child: Column(
        children: [
          const SizedBox(height: 20),
          
          // Ï†úÏ∂ú Î≤ÑÌäº
          if (!puzzleState.isCompleted) ...[
            SizedBox(
              width: double.infinity,
              height: 56,
